const contractAddress = '0xd010910f2a4310a4da0F81b4cE8A796e4ce4CdE0'; // Replace with your contract address

const web3 = new Web3(Web3.givenProvider || "http://localhost:8545");
const communityTokenContract = new web3.eth.Contract(contractABI, contractAddress);

async function getAccount() {
    const accounts = await web3.eth.getAccounts();
    return accounts[0];
}

async function updateRewardBalance() {
    const account = await getAccount();
    const rewardBalance = await communityTokenContract.methods.getRewardBalance(account).call();
    document.getElementById('rewardBalance').innerText = `Reward Balance: ${web3.utils.fromWei(rewardBalance, 'ether')} CTK`;
}

document.getElementById('transferButton').onclick = async () => {
    const to = document.getElementById('transferTo').value;
    const amount = web3.utils.toWei(document.getElementById('transferAmount').value, 'ether');
    const account = await getAccount();

    await communityTokenContract.methods.transfer(to, amount).send({ from: account });
    alert('Transfer successful!');
};

